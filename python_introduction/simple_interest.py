# This script calculates the simple interest earned on an investment.
# The formula for simple interest is I = P * R * T, where:
# I = Interest earned
# P = Principal amount (initial investment)
# R = Annual interest rate (as a decimal)
# T = Time the money is invested for in years

# Define the variables for principal, rate, and time
principal = 1000  # P: representing $1000
rate = 0.05       # R: representing 5% annual interest rate (as a decimal)
time = 3          # T: representing 3 years

# Calculate the simple interest using the formula I = P * R * T
interest = principal * rate * time

# Print the calculated interest in the specified format
print(f"The simple interest is: {interest}")
