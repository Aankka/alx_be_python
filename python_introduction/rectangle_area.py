# This script calculates the area of a rectangle.
# The area is found by multiplying its length by its width.

# Define the variables for length and width of the rectangle
length = 10  # Length of the rectangle
width = 5    # Width of the rectangle

# Calculate the area of the rectangle using the formula: Area = length * width
area = length * width

# Print the calculated area in the specified format
print(f"The area of the rectangle is: {area}")
