# This script converts a given number of hours into seconds.
# There are 3600 seconds in an hour (60 minutes * 60 seconds).

# Define the variable for the number of hours to convert
hours = 2  # Number of hours

# Calculate the number of seconds in the given hours
# 1 hour = 60 minutes * 60 seconds = 3600 seconds
seconds = hours * 3600

# Print the result in the specified format
print(f"{hours} hour(s) is {seconds} seconds.")
